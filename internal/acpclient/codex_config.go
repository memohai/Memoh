const (
	codexAgentID                    = "codex"
	codexManagedAPIKeyProviderID    = "OpenAI"
	codexManagedAPIKeyProviderName  = "OpenAI"
	codexManagedOAuthProviderID     = "chatgpt-http"
	codexManagedOAuthProviderName   = "ChatGPT HTTP"
	codexDefaultOpenAIBaseURL       = "https://api.openai.com/v1"
	codexDefaultChatGPTCodexBaseURL = "https://chatgpt.com/backend-api/codex"
	codexManagedConfigTemplate      = `# Generated by Memoh for managed Codex ACP sessions.
model_provider = {{ tomlString .ProviderID }}
model_reasoning_effort = "xhigh"
model_reasoning_summary = "detailed"
model_supports_reasoning_summaries = true
hide_agent_reasoning = false
show_raw_agent_reasoning = false

[model_providers.{{ .ProviderID }}]
name = {{ tomlString .ProviderName }}
base_url = {{ tomlString .BaseURL }}
wire_api = "responses"
requires_openai_auth = {{ .RequiresOpenAIAuth }}
supports_websockets = false
`
)

// CodexManagedConfigDir is the standard Codex config directory inside the
// managed container workspace.
const CodexManagedConfigDir = dataMountPath + "/.codex"

var codexConfigTemplate = template.Must(template.New("codex-managed-config").
	Funcs(template.FuncMap{"tomlString": strconv.Quote}).
	Parse(codexManagedConfigTemplate))

var (
	ErrCodexOAuthIncomplete  = errors.New("codex OAuth is incomplete")
	ErrCodexAuthTokenMissing = errors.New("codex auth token is missing")
)

type codexManagedConfigData struct {
	ProviderID         string
	ProviderName       string
	BaseURL            string
	RequiresOpenAIAuth bool
}

type CodexManagedConfig struct {
	Mode    SetupMode
	Managed map[string]string
	OAuth   *CodexOAuthCredentials
}

type CodexOAuthCredentials struct {
	AccessToken  string //nolint:gosec // runtime credential material used to construct Codex auth.json
	IDToken      string
	RefreshToken string //nolint:gosec // runtime credential material used to construct Codex auth.json
	AccountID    string
	BaseURL      string
	ExpiresAt    time.Time
	LastRefresh  time.Time
}

type CodexOAuthAuthStatus struct {
	Valid     bool
	AccountID string
}

// WriteCodexManagedConfig writes the fixed Codex config.toml and auth.json
// used by Memoh-managed Codex ACP sessions.
func WriteCodexManagedConfig(ctx context.Context, client *bridge.Client, managed map[string]string) error {
	return WriteCodexManagedConfigWithAuth(ctx, client, CodexManagedConfig{
		Mode:    SetupModeAPIKey,
		Managed: managed,
	})
}

func WriteCodexManagedConfigWithAuth(ctx context.Context, client *bridge.Client, cfg CodexManagedConfig) error {
	if client == nil {
		return errors.New("workspace bridge client is required")
	}
	auth, err := renderCodexManagedAuth(cfg)
	if err != nil {
		return fmt.Errorf("render Codex auth: %w", err)
	}
	content, err := renderCodexManagedConfig(cfg)
	if err != nil {
		return fmt.Errorf("render Codex config: %w", err)
	}
	if err := client.WriteFile(ctx, path.Join(CodexManagedConfigDir, "auth.json"), auth); err != nil {
		return fmt.Errorf("write Codex auth: %w", err)
	}
	if err := client.WriteFile(ctx, path.Join(CodexManagedConfigDir, "config.toml"), content); err != nil {
		return fmt.Errorf("write Codex config: %w", err)
	}
	return nil
}

// WriteCodexManagedConfigFile writes only config.toml. It is used to refresh
// managed Codex runtime behavior without touching an existing OAuth auth.json.
// When refreshing an OAuth config without credentials in hand, the provider
// base_url already written at setup time is preserved instead of being reset
// to the default ChatGPT endpoint.
func WriteCodexManagedConfigFile(ctx context.Context, client *bridge.Client, cfg CodexManagedConfig) error {
	if client == nil {
		return errors.New("workspace bridge client is required")
	}
	if normalizeSetupMode(cfg.Mode) == SetupModeOAuth && cfg.OAuth == nil {
		if baseURL := readCodexManagedOAuthBaseURL(ctx, client); baseURL != "" {
			cfg.OAuth = &CodexOAuthCredentials{BaseURL: baseURL}
		}
	}
	content, err := renderCodexManagedConfig(cfg)
	if err != nil {
		return fmt.Errorf("render Codex config: %w", err)
	}
	if err := client.WriteFile(ctx, path.Join(CodexManagedConfigDir, "config.toml"), content); err != nil {
		return fmt.Errorf("write Codex config: %w", err)
	}
	return nil
}

// IsCodexManagedOAuthConfig reports whether config.toml currently points at
// Memoh's managed ChatGPT OAuth provider.
func IsCodexManagedOAuthConfig(ctx context.Context, client *bridge.Client) bool {
	return readCodexManagedConfig(ctx, client).ProviderID == codexManagedOAuthProviderID
}

func CheckCodexManagedOAuthAuth(ctx context.Context, client *bridge.Client) (CodexOAuthAuthStatus, error) {
	if client == nil {
		return CodexOAuthAuthStatus{}, errors.New("workspace bridge client is required")
	}
	resp, err := client.ReadFile(ctx, path.Join(CodexManagedConfigDir, "auth.json"), 0, 0)
	if err != nil {
		return CodexOAuthAuthStatus{}, ErrCodexOAuthIncomplete
	}
	status := ParseCodexOAuthAuth(resp.GetContent())
	if !status.Valid {
		return status, ErrCodexAuthTokenMissing
	}
	return status, nil
}

func ParseCodexOAuthAuth(content string) CodexOAuthAuthStatus {
	var auth struct {
		AuthMode string `json:"auth_mode"`
		Tokens   struct {
			IDToken      string `json:"id_token"`
			AccessToken  string `json:"access_token"`  //nolint:gosec // Codex auth status parses existing runtime token material.
			RefreshToken string `json:"refresh_token"` //nolint:gosec // Codex auth status parses existing runtime token material.
			AccountID    string `json:"account_id"`
		} `json:"tokens"`
	}
	if err := json.Unmarshal([]byte(content), &auth); err != nil {
		return CodexOAuthAuthStatus{}
	}
	idToken := strings.TrimSpace(auth.Tokens.IDToken)
	accessToken := strings.TrimSpace(auth.Tokens.AccessToken)
	refreshToken := strings.TrimSpace(auth.Tokens.RefreshToken)
	accountID := strings.TrimSpace(auth.Tokens.AccountID)
	return CodexOAuthAuthStatus{
		Valid: strings.EqualFold(strings.TrimSpace(auth.AuthMode), "chatgpt") &&
			idToken != "" &&
			accessToken != "" &&
			refreshToken != "" &&
			accountID != "" &&
			idToken != accessToken,
		AccountID: accountID,
	}
}

// readCodexManagedOAuthBaseURL extracts the provider base_url from an existing
// managed config.toml, but only when that config was rendered for the OAuth
// provider — an api_key-mode leftover must not leak its OpenAI URL into an
// OAuth refresh.
func readCodexManagedOAuthBaseURL(ctx context.Context, client *bridge.Client) string {
	config := readCodexManagedConfig(ctx, client)
	if config.ProviderID != codexManagedOAuthProviderID {
		return ""
	}
	return config.BaseURL
}

type codexManagedConfigSummary struct {
	ProviderID string
	BaseURL    string
}

func readCodexManagedConfig(ctx context.Context, client *bridge.Client) codexManagedConfigSummary {
	rc, err := client.ReadRaw(ctx, path.Join(CodexManagedConfigDir, "config.toml"))
	if err != nil {
		return codexManagedConfigSummary{}
	}
	defer func() { _ = rc.Close() }()
	data, err := io.ReadAll(io.LimitReader(rc, 64*1024))
	if err != nil {
		return codexManagedConfigSummary{}
	}
	var out codexManagedConfigSummary
	for _, line := range strings.Split(string(data), "\n") {
		switch key, value := parseCodexConfigStringLine(line); key {
		case "model_provider":
			out.ProviderID = value
		case "base_url":
			out.BaseURL = value
		}
	}
	return out
}

// parseCodexConfigStringLine parses a `key = "quoted value"` line from a
// config.toml generated by codexManagedConfigTemplate.
func parseCodexConfigStringLine(line string) (string, string) {
	key, rawValue, ok := strings.Cut(strings.TrimSpace(line), "=")
	if !ok {
		return "", ""
	}
	value, err := strconv.Unquote(strings.TrimSpace(rawValue))
	if err != nil {
		return "", ""
	}
	return strings.TrimSpace(key), value
}

func renderCodexManagedConfig(cfg CodexManagedConfig) ([]byte, error) {
	data := codexManagedConfigData{
		ProviderID:   codexManagedAPIKeyProviderID,
		ProviderName: codexManagedAPIKeyProviderName,
		BaseURL:      strings.TrimSpace(cfg.Managed["base_url"]),
	}
	switch normalizeSetupMode(cfg.Mode) {
	case SetupModeOAuth:
		data.ProviderID = codexManagedOAuthProviderID
		data.ProviderName = codexManagedOAuthProviderName
		data.BaseURL = codexOAuthProviderBaseURL(cfg.OAuth)
		data.RequiresOpenAIAuth = true
	default:
		if data.BaseURL == "" {
			data.BaseURL = codexDefaultOpenAIBaseURL
		}
	}

	var out bytes.Buffer
	if err := codexConfigTemplate.Execute(&out, data); err != nil {
		return nil, err
	}
	return out.Bytes(), nil
}

func codexOAuthProviderBaseURL(creds *CodexOAuthCredentials) string {
	if creds == nil {
		return codexDefaultChatGPTCodexBaseURL
	}
	baseURL := strings.TrimRight(strings.TrimSpace(creds.BaseURL), "/")
	if baseURL == "" {
		return codexDefaultChatGPTCodexBaseURL
	}
	if strings.HasSuffix(baseURL, "/codex") {
		return baseURL
	}
	if strings.HasSuffix(baseURL, "/backend-api") {
		return baseURL + "/codex"
	}
	return baseURL
}

func renderCodexManagedAuth(cfg CodexManagedConfig) ([]byte, error) {
	switch normalizeSetupMode(cfg.Mode) {
	case SetupModeOAuth:
		return renderCodexManagedOAuthAuth(cfg.OAuth)
	default:
		return renderCodexManagedAPIKeyAuth(cfg.Managed)
	}
}

func renderCodexManagedAPIKeyAuth(managed map[string]string) ([]byte, error) {
	apiKey := strings.TrimSpace(managed["api_key"])
	if apiKey == "" {
		return nil, errors.New("api_key is required")
	}
	content, err := json.MarshalIndent(map[string]string{
		"OPENAI_API_KEY": apiKey,
	}, "", "  ")
	if err != nil {
		return nil, err
	}
	return append(content, '\n'), nil
}

func renderCodexManagedOAuthAuth(creds *CodexOAuthCredentials) ([]byte, error) {
	if creds == nil {
		return nil, errors.New("oauth credentials are required")
	}
	accessToken := strings.TrimSpace(creds.AccessToken)
	if accessToken == "" {
		return nil, errors.New("oauth access token is required")
	}
	idToken := strings.TrimSpace(creds.IDToken)
	if idToken == "" {
		return nil, errors.New("oauth id token is required")
	}
	accountID := strings.TrimSpace(creds.AccountID)
	tokens := map[string]string{
		"id_token":      idToken,
		"access_token":  accessToken,
		"refresh_token": strings.TrimSpace(creds.RefreshToken),
	}
	if accountID != "" {
		tokens["account_id"] = accountID
	}
	lastRefresh := creds.LastRefresh
	if lastRefresh.IsZero() {
		lastRefresh = time.Now().UTC()
	}
	content, err := json.MarshalIndent(map[string]any{
		"auth_mode":    "chatgpt",
		"tokens":       tokens,
		"last_refresh": lastRefresh.UTC().Format(time.RFC3339Nano),
	}, "", "  ")
	if err != nil {
		return nil, err
	}
	return append(content, '\n'), nil
}

func isCodexAgent(agentID string) bool {
	return strings.EqualFold(strings.TrimSpace(agentID), codexAgentID)
}
